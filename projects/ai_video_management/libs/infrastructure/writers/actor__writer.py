    def generate_batch(
        self,
        attrs: ActorAttrs,
        count: int,
        resolution: str = DEFAULT_RESOLUTION,
        seeds: list[int] | None = None,
        archetype: str | None = None,
    ) -> GenerateResult:
        """Per follow-up 059: `archetype` slug (when provided) is written
        to the sidecar `archetype` row for each generated actor in this
        batch. Standard-mode callers pass None (no archetype row written;
        startup `migrate_archetypes()` will reverse-classify on first boot).
        Diverse-mode preview-then-confirm path (follow-up 059) passes the
        per-slot rolled archetype so each actor's sidecar carries it from
        first write."""
        self._validate_attrs(attrs)
        if not MIN_BATCH_COUNT <= count <= MAX_BATCH_COUNT:
            raise InvalidAttribute(
                f"count must be between {MIN_BATCH_COUNT} and {MAX_BATCH_COUNT}, got {count}"
            )
        if resolution not in RESOLUTION_OPTIONS:
            raise InvalidAttribute(
                f"resolution={resolution!r} not in {sorted(RESOLUTION_OPTIONS)}"
            )
        if seeds is not None:
            if not isinstance(seeds, list) or len(seeds) != count:
                raise InvalidAttribute(
                    f"seeds must be a list of length {count}, got {seeds!r}"
                )
            for s in seeds:
                if not isinstance(s, int):
                    raise InvalidAttribute(f"seeds must be all int, got {seeds!r}")
        actors_dir = self.actors_dir()
        try:
            actors_dir.mkdir(parents=True, exist_ok=True)
        except OSError as exc:
            raise GenerationDirMissing(str(exc))

        result = GenerateResult()
        base_seed = int(time.time() * 1000)
        target_px = _RESOLUTION_PRESETS[resolution]
        self._reap_incomplete_folders(actors_dir)
        for i in range(count):
            seed = seeds[i] if seeds is not None else base_seed + i
            try:
                actor_id, actor_folder = self._allocate_actor_id(actors_dir)
            except GenerationDirMissing as exc:
                result.errors.append({"requested_id": f"slot_{i}", "message": f"alloc_failed: {exc}"})
                continue
            face_prompt = self._build_face_prompt(attrs, seed, archetype)
            body_prompt = self._build_body_prompt(attrs, seed, archetype)
            # --- Face shot (1:1) — primary identity image ---
            try:
                face_bytes = self._provider.generate(face_prompt, seed, IMAGE_WIDTH, IMAGE_HEIGHT)
            except Exception as exc:
                result.errors.append({"requested_id": actor_id, "message": f"http_failed: {exc}"})
                self._cleanup_empty_folder(actor_folder)
                continue
            if not face_bytes:
                result.errors.append({"requested_id": actor_id, "message": "empty_response"})
                self._cleanup_empty_folder(actor_folder)
                continue
            if target_px is not None:
                try:
                    face_bytes = self._resize_jpeg(face_bytes, target_px)
                except Exception as exc:
                    result.errors.append({"requested_id": actor_id, "message": f"resize_failed: {exc}"})
                    self._cleanup_empty_folder(actor_folder)
                    continue
            face_path = actor_folder / _attrs_to_filename(attrs)
            md_path = actor_folder / f"{actor_id}.md"
            # --- Body shot (9:16) — supplementary casting reference (follow-up 052) ---
            # Same seed → identity coherence with face. Failure isolated: face is still saved.
            body_path: Path | None = None
            body_error: str | None = None
            try:
                body_bytes = self._provider.generate(
                    body_prompt, seed, IMAGE_WIDTH_BODY, IMAGE_HEIGHT_BODY
                )
            except Exception as exc:
                body_bytes = b""
                body_error = f"body_http_failed: {exc}"
            if not body_bytes and body_error is None:
                body_error = "body_empty_response"
            if body_bytes:
                # Body shots skip the resolution-preset upscale path — 9:16 width/height
                # would need a different resize math, and body is a casting reference
                # (no print-quality requirement). Sidecar records body_resolution=normal.
                body_path = actor_folder / _attrs_to_body_filename(attrs)
            try:
                face_path.write_bytes(face_bytes)
                if body_path is not None:
                    try:
                        body_path.write_bytes(body_bytes)
                    except OSError as exc:
                        body_error = f"body_write_failed: {exc}"
                        body_path = None
                md_path.write_text(
                    self._build_sidecar(
                        actor_id, attrs, face_prompt, body_prompt, seed, resolution,
                        body_filename=body_path.name if body_path is not None else None,
                        archetype=archetype,
                    ),
                    encoding="utf-8",
                )
            except OSError as exc:
                result.errors.append({"requested_id": actor_id, "message": f"write_failed: {exc}"})
                self._cleanup_empty_folder(actor_folder)
                continue
            if body_error is not None:
                result.errors.append({"requested_id": actor_id, "message": body_error})
            rel_image = self._rel(face_path)
            result.generated.append(
                {
                    "id": actor_id,
                    "image_path": rel_image,
                    "body_path": self._rel(body_path) if body_path is not None else None,
                    "attrs": attrs.to_dict(),
                    "seed": seed,
                    "resolution": resolution,
                    "archetype": archetype,
                }
            )
        return result

    @staticmethod
    def _resize_jpeg(jpeg_bytes: bytes, target_px: int) -> bytes:
        """Resize a JPEG to target_px × target_px using Lanczos resampling.

        Per follow-up 029: Kling returns ~1024×1024 natively for 1:1 aspect;
        the user-pickable resolution dropdown ("2k"/"4k") forces an explicit
        upscale via Pillow's high-quality LANCZOS filter so the saved file
        matches the requested pixel count. Upscaling does not add true
        detail — it is interpolation; recorded in the sidecar.
        """
        img = Image.open(BytesIO(jpeg_bytes)).convert("RGB")
        img = img.resize((target_px, target_px), Image.LANCZOS)
        buf = BytesIO()
        img.save(buf, format="JPEG", quality=JPEG_QUALITY)
        return buf.getvalue()

