    def _allocate_actor_id(self, actors_dir: Path) -> tuple[str, Path]:
        """Atomically claim the next free actor_NNNN id via mkdir(exist_ok=False).

        Per follow-up 027: replaces the old pre-computed `next_id + offset`
        pattern, which lost to itself when 9 concurrent count=1 requests
        all saw the same starting id and raced on the same mkdir. Now each
        allocation walks forward through ids using mkdir as the atomic
        primitive — first thread to win mkdir on actor_K owns it, others
        try actor_K+1, K+2, ... up to _MAX_ID_ALLOC_SCAN attempts.
        """
        start = self._next_actor_id_num(actors_dir)
        candidate = start
        attempts = 0
        while attempts < _MAX_ID_ALLOC_SCAN:
            actor_id = f"actor_{candidate:04d}"
            actor_folder = actors_dir / actor_id
            try:
                actor_folder.mkdir(parents=True, exist_ok=False)
                return actor_id, actor_folder
            except FileExistsError:
                candidate += 1
                attempts += 1
                continue
            except OSError as exc:
                raise GenerationDirMissing(str(exc)) from exc
        raise GenerationDirMissing(
            f"exhausted {_MAX_ID_ALLOC_SCAN} id-allocation attempts starting from actor_{start:04d}"
        )

    @staticmethod
    def _reap_incomplete_folders(actors_dir: Path) -> None:
        """Drop actor_NNNN folders left behind by killed batches (no jpg).

        Per follow-up 018 + 027 + 033: "no jpg" check uses `_find_actor_jpg`
        which matches both the follow-up 033 descriptive filename and the
        legacy `actor_id.jpg`, so a partly-migrated tree does not get reaped.

        Per follow-up 073: reaper now respects a mtime threshold
        (`_REAP_MIN_AGE_SECONDS`). Folders younger than the threshold are
        likely being worked on by a concurrent `generate_batch` call (the
        frontend per follow-up 064 issues N parallel `count=1` requests via
        a worker pool; each request's `_reap_incomplete_folders` runs before
        the OTHER requests have finished their slow Kling HTTP and written
        the jpg). Without the mtime guard, the reaper deletes peer
        in-flight folders, then the peer's `write_bytes` fails with
        `[Errno 2] No such file or directory`. The threshold (5 minutes) is
        safely past Kling's 120s face-wait + 120s body-wait worst case.
        """
        try:
            entries = list(actors_dir.iterdir())
        except OSError:
            return
        cutoff = time.time() - _REAP_MIN_AGE_SECONDS
        for entry in entries:
            if not entry.is_dir():
                continue
            if not _ACTOR_DIR_RE.match(entry.name):
                continue
            if _find_actor_jpg(entry) is not None:
                continue
            try:
                if entry.stat().st_mtime > cutoff:
                    continue  # in-flight (or very recently allocated) — skip
            except OSError:
                continue
            try:
                for child in entry.iterdir():
                    try:
                        child.unlink()
                    except OSError:
                        pass
                entry.rmdir()
            except OSError:
                pass

    @staticmethod
    def _next_actor_id_num(actors_dir: Path) -> int:
        """Return max+1 across every actor_NNNN folder under actors_dir.

        Pure scan with no side effects (per follow-up 027: reaping moved
        to `_reap_incomplete_folders` so the allocator's mkdir is the
        sole atomic primitive). A folder counts as occupied regardless of
        jpg presence — if a folder exists, its id is taken.
        """
        max_num = 0
        try:
            entries = list(actors_dir.iterdir())
        except OSError:
            return 1
        for entry in entries:
            if not entry.is_dir():
                continue
            m = _ACTOR_DIR_RE.match(entry.name)
            if not m:
                continue
            n = int(m.group(1))
            if n > max_num:
                max_num = n
        return max_num + 1

    @staticmethod
    def _build_sidecar(
        actor_id: str,
        attrs: ActorAttrs,
        face_prompt: str,
        body_prompt: str,
        seed: int,
        resolution: str = DEFAULT_RESOLUTION,
        body_filename: str | None = None,
        archetype: str | None = None,
    ) -> str:
        """Per follow-up 052: sidecar md now records BOTH the face prompt and
        the body prompt + the body image filename (when body gen succeeded).
        Per follow-up 053: optional `archetype` slug recorded in the attr
        table for diverse-mode generated actors (and back-filled into legacy
        sidecars by `migrate_archetypes()`).
        Face / body share the same seed + variance — both prompts let users
        reproduce either image independently."""
        notes_display = attrs.notes if attrs.notes else "—"
        body_image_display = body_filename if body_filename else "— (body gen failed)"
        archetype_row = f"| archetype | {archetype} |\n" if archetype else ""
        return (
            f"# {actor_id}\n"
            f"\n"
            f"AI-generated actor face + body for casting (Kling text-to-image,\n"
            f"follow-up 025 + 029 + 052 + 053).\n"
            f"\n"
            f"| 字段 | 值 |\n"
            f"|---|---|\n"
            f"| ethnicity | {attrs.ethnicity} |\n"
            f"| gender | {attrs.gender} |\n"
            f"| age_range | {attrs.age_range} |\n"
            f"| look | {attrs.look} |\n"
            f"| style | {attrs.style} |\n"
            f"| notes | {notes_display} |\n"
            f"| seed | {seed} |\n"
            f"| resolution | {resolution} |\n"
            f"| body_image | {body_image_display} |\n"
            f"| body_resolution | normal |\n"
            f"{archetype_row}"
            f"\n"
            f"## 生成 prompt (face shot)\n"
            f"\n"
            f"```\n"
            f"{face_prompt}\n"
            f"```\n"
            f"\n"
            f"## 生成 prompt (body shot)\n"
            f"\n"
            f"```\n"
            f"{body_prompt}\n"
            f"```\n"
        )

    @staticmethod
    def _parse_sidecar(md_path: Path) -> ActorAttrs | None:
        parsed = ActorPool._parse_sidecar_full(md_path)
        return parsed[0] if parsed is not None else None

    @staticmethod
    def _parse_sidecar_full(md_path: Path) -> tuple[ActorAttrs, str | None] | None:
        """Per follow-up 053: returns `(attrs, archetype_slug | None)`. The
        archetype slug is None for legacy sidecars (pre-053) until the
        startup migration writes it back; valid slugs come from
        `_ARCHETYPE_BY_SLUG`."""
        try:
            text = md_path.read_text(encoding="utf-8")
        except OSError:
            return None
        fields: dict[str, str] = {}
        for line in text.splitlines():
            line = line.strip()
            if not line.startswith("|"):
                continue
            cells = [c.strip() for c in line.strip("|").split("|")]
            if len(cells) != 2:
                continue
            key, value = cells[0], cells[1]
            if key in {"ethnicity", "gender", "age_range", "look", "style", "notes", "archetype"}:
                fields[key] = value if value != "—" else ""
        try:
            attrs = ActorAttrs(
                ethnicity=fields["ethnicity"],
                gender=fields["gender"],
                age_range=fields["age_range"],
                look=fields["look"],
                style=fields["style"],
                notes=fields.get("notes", ""),
            )
        except KeyError:
            return None
        archetype_value = fields.get("archetype", "").strip() or None
        if archetype_value is not None and archetype_value not in _ARCHETYPE_BY_SLUG:
            archetype_value = None
        return attrs, archetype_value

    def _rel(self, p: Path) -> str:
        try:
            return p.resolve().relative_to(self._resolver.root).as_posix()
        except (OSError, ValueError):
            return p.as_posix()

    @staticmethod
    def _cleanup_empty_folder(folder: Path) -> None:
        try:
            if folder.is_dir() and not any(folder.iterdir()):
                folder.rmdir()
        except OSError:
            pass


