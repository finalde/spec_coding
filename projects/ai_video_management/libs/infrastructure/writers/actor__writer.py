def _attrs_to_filename(attrs: "ActorAttrs") -> str:
    """Per follow-up 033: jpg files named {ethnicity}__{gender}__{age_range}.jpg
    inside the actor's folder. Double-underscore separator avoids collision
    with css-class naming and is distinctive enough to spot at a glance."""
    return f"{attrs.ethnicity}__{attrs.gender}__{attrs.age_range}.jpg"


def _attrs_to_body_filename(attrs: "ActorAttrs") -> str:
    """Per follow-up 052: full-body casting shot saved alongside the face jpg as
    `{ethnicity}__{gender}__{age_range}__body.jpg`. Suffix `__body` is checked
    by `_find_actor_jpg` and `_find_actor_body_jpg` to keep face / body lookup
    disjoint."""
    return f"{attrs.ethnicity}__{attrs.gender}__{attrs.age_range}{BODY_FILENAME_SUFFIX}.jpg"


def _find_actor_jpg(actor_folder: Path) -> Path | None:
    """Return the actor's face jpg file path, preferring the follow-up 033
    descriptive naming over the legacy `{actor_id}.jpg`. Per follow-up 052:
    explicitly excludes `__body.jpg` since the body shot lives in the same
    folder. Returns None if the folder contains no eligible jpg."""
    for child in actor_folder.iterdir() if actor_folder.is_dir() else []:
        if not child.is_file():
            continue
        if _BODY_SUFFIX_RE.search(child.name):
            continue
        if _NEW_FILENAME_RE.match(child.name):
            return child
    # Fallback: legacy `{actor_id}.jpg` for not-yet-migrated folders.
    legacy = actor_folder / f"{actor_folder.name}.jpg"
    if legacy.is_file():
        return legacy
    return None


def _find_actor_body_jpg(actor_folder: Path) -> Path | None:
    """Per follow-up 052: locate the actor's body jpg (`*__body.jpg`).
    Returns None if no body shot was generated (e.g. body call failed during
    `generate_batch`)."""
    if not actor_folder.is_dir():
        return None
    for child in actor_folder.iterdir():
        if child.is_file() and _BODY_SUFFIX_RE.search(child.name):
            return child
    return None

