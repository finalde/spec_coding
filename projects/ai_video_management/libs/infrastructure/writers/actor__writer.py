_BIAS_WILD_PROB: float = 0.25


def _pick_biased(rng: random.Random, pool: tuple[str, ...], biased: tuple[int, ...] | None) -> str:
    """Per follow-up 069 + 074: random.choice over a biased index subset
    when one is provided; uniform random over the full pool otherwise.
    Out-of-range indices are silently skipped (defends against pool
    reordering).

    Per follow-up 074: with probability `_BIAS_WILD_PROB` the call falls
    through to uniform random over the FULL pool even when a bias is
    given. The user's feedback was that within-archetype actors look too
    same; with 6 biased facial features and wild_prob=0.25, the chance
    that all 6 stay archetype-biased is 0.75**6 ≈ 18%, so most generated
    actors have at least one "wild" feature breaking sameness while the
    archetype still shapes the overall look. Pure deterministic — seed
    reproduces the same choice.
    """
    if biased and rng.random() >= _BIAS_WILD_PROB:
        candidates = [pool[i] for i in biased if 0 <= i < len(pool)]
        if candidates:
            return rng.choice(candidates)
    return rng.choice(pool)

_ARCHETYPE_FALLBACK_SLUG: str = "everyman"


def _eligible_archetypes(gender: str) -> tuple[ArchetypeSpec, ...]:
    """Per follow-up 053: filter `_ARCHETYPES` to those whose `gender_filter`
    is `both` or matches the requested gender. The returned tuple preserves
    `_ARCHETYPES` order so distribution is deterministic for a given gender.
    """
    return tuple(a for a in _ARCHETYPES if a.gender_filter in ("both", gender))


def _classify_actor_attrs(attrs: "ActorAttrs") -> str:
    """Per follow-up 053: best-effort reverse-lookup from `(gender, age_range,
    look, style)` → archetype slug for the startup migration of pre-053
    actors. Walks `_ARCHETYPES` in order; first match wins; falls through to
    `_ARCHETYPE_FALLBACK_SLUG` ("everyman") so EVERY existing actor gets an
    archetype tag without surfacing None."""
    for spec in _ARCHETYPES:
        if spec.gender_filter not in ("both", attrs.gender):
            continue
        if attrs.age_range not in spec.age_ranges:
            continue
        if attrs.look not in spec.looks:
            continue
        if attrs.style not in spec.styles:
            continue
        return spec.slug
    return _ARCHETYPE_FALLBACK_SLUG


