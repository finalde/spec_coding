KLING_MAX_WAIT_SECONDS: float = 120.0


def _b64url(data: bytes) -> str:
    """URL-safe base64 without padding (JWT segment encoding)."""
    return base64.urlsafe_b64encode(data).rstrip(b"=").decode("ascii")


def _make_kling_jwt(access_key: str, secret_key: str, exp_seconds: int = KLING_JWT_EXP_SECONDS) -> str:
    """Build an HS256-signed JWT for Kling API auth, using only stdlib.

    Avoids `PyJWT` dependency. The Kling spec requires iss/exp/nbf claims.
    """
    header = {"alg": "HS256", "typ": "JWT"}
    now = int(time.time())
    payload = {"iss": access_key, "exp": now + exp_seconds, "nbf": now - 5}
    header_b64 = _b64url(json.dumps(header, separators=(",", ":")).encode("utf-8"))
    payload_b64 = _b64url(json.dumps(payload, separators=(",", ":")).encode("utf-8"))
    signing_input = f"{header_b64}.{payload_b64}".encode("ascii")
    signature = hmac.new(secret_key.encode("utf-8"), signing_input, hashlib.sha256).digest()
    sig_b64 = _b64url(signature)
    return f"{header_b64}.{payload_b64}.{sig_b64}"


def _kling_aspect_ratio(width: int, height: int) -> str:
    """Map (width, height) to a Kling-accepted aspect ratio enum.

    Kling does not accept arbitrary dims; it accepts a fixed set of ratio
    enums. Approximate by closest-match.
    """
    if height == 0:
        return "1:1"
    ratio = width / height
    candidates = {
        "1:1": 1.0,
        "16:9": 16 / 9,
        "9:16": 9 / 16,
        "4:3": 4 / 3,
        "3:4": 3 / 4,
    }
    return min(candidates, key=lambda k: abs(candidates[k] - ratio))


def _is_safe_download_host(url: str) -> bool:
    """Reject SSRF-prone hostnames (localhost, private ranges) on Kling's CDN URL."""
    try:
        parsed = urlparse(url)
    except ValueError:
        return False
    if parsed.scheme != "https":
        return False
    host = parsed.hostname
    if not host:
        return False
    try:
        addr_infos = socket.getaddrinfo(host, None)
    except OSError:
        return False
    for info in addr_infos:
        sockaddr = info[4]
        ip_str = sockaddr[0]
        try:
            ip = ipaddress.ip_address(ip_str)
        except ValueError:
            continue
        if ip.is_loopback or ip.is_private or ip.is_link_local or ip.is_multicast or ip.is_reserved:
            return False
    return True


class KlingProvider:
    """Async POST→poll→download against api.klingai.com text-to-image.

    Flow:
      1. POST /images/generations  →  {code: 0, data: {task_id: <uuid>}}
      2. Poll GET /images/generations?pageSize=500 every 2s; find our task
         by task_id; wait for task_status="succeed" (or "failed" → raise).
      3. task_result.images[0].url → SSRF-vet → download bytes.
    """

    name: str = "kling"

    def __init__(
        self,
        access_key: str,
        secret_key: str,
        model: str = KLING_DEFAULT_MODEL,
        base_url: str = KLING_BASE_URL,
        poll_interval: float = KLING_POLL_INTERVAL_SECONDS,
        max_wait: float = KLING_MAX_WAIT_SECONDS,
    ) -> None:
        if not access_key or not secret_key:
            raise ValueError("KlingProvider requires both access_key and secret_key")
        self._ak = access_key
        self._sk = secret_key
        self._model = model
        self._base = base_url.rstrip("/")
        self._poll_interval = poll_interval
        self._max_wait = max_wait

    @classmethod
    def from_env(cls) -> "KlingProvider | None":
        ak = os.environ.get(KLING_ACCESS_KEY_ENV, "").strip()
        sk = os.environ.get(KLING_SECRET_KEY_ENV, "").strip()
        if not ak or not sk:
            return None
        return cls(ak, sk)

    def generate(self, prompt: str, seed: int, width: int, height: int) -> bytes:
        token = _make_kling_jwt(self._ak, self._sk)
        with httpx.Client(timeout=DEFAULT_TIMEOUT_SECONDS, follow_redirects=False) as client:
            task_id = self._submit(client, token, prompt, seed, width, height)
            img_url = self._poll(client, token, task_id)
        if not _is_safe_download_host(img_url):
            raise RuntimeError(f"kling: rejected unsafe download URL: {img_url}")
        with httpx.Client(timeout=DEFAULT_TIMEOUT_SECONDS, follow_redirects=True) as client:
            with client.stream("GET", img_url) as resp:
                resp.raise_for_status()
                buf = bytearray()
                for chunk in resp.iter_bytes():
                    buf.extend(chunk)
                    if len(buf) > MAX_RESPONSE_BYTES:
                        raise ValueError(
                            f"kling: response_too_large (>{MAX_RESPONSE_BYTES} bytes)"
                        )
                return bytes(buf)

    def _submit(
        self,
        client: httpx.Client,
        token: str,
        prompt: str,
        seed: int,
        width: int,
        height: int,
    ) -> str:
        body: dict[str, object] = {
            "model_name": self._model,
            "prompt": prompt,
            "aspect_ratio": _kling_aspect_ratio(width, height),
            "n": 1,
        }
        resp = client.post(
            f"{self._base}/images/generations",
            json=body,
            headers={
                "Authorization": f"Bearer {token}",
                "Content-Type": "application/json",
            },
        )
        resp.raise_for_status()
        payload = resp.json()
        if payload.get("code") != 0:
            raise RuntimeError(
                f"kling submit: code={payload.get('code')} message={payload.get('message')!r}"
            )
        data = payload.get("data") or {}
        task_id = data.get("task_id")
        if not isinstance(task_id, str) or not task_id:
            raise RuntimeError(f"kling submit: missing task_id in {payload!r}")
        return task_id

    def _poll(self, client: httpx.Client, token: str, task_id: str) -> str:
        start = time.time()
        headers = {"Authorization": f"Bearer {token}"}
        while True:
            if time.time() - start > self._max_wait:
                raise TimeoutError(
                    f"kling: task {task_id} not done in {self._max_wait:.0f}s"
                )
            resp = client.get(
                f"{self._base}/images/generations",
                params={"pageSize": 500},
                headers=headers,
            )
            resp.raise_for_status()
            tasks = (resp.json() or {}).get("data") or []
            for task in tasks:
                if task.get("task_id") != task_id:
                    continue
                status = task.get("task_status")
                if status == "succeed":
                    images = (task.get("task_result") or {}).get("images") or []
                    if not images:
                        raise RuntimeError(
                            f"kling: task {task_id} succeeded with no images"
                        )
                    url = images[0].get("url")
                    if not isinstance(url, str) or not url:
                        raise RuntimeError(
                            f"kling: task {task_id} missing image url"
                        )
                    return url
                if status == "failed":
                    msg = task.get("task_status_msg") or task
                    raise RuntimeError(f"kling: task {task_id} failed: {msg!r}")
                break
            time.sleep(self._poll_interval)


