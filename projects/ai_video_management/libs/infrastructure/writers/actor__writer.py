    def migrate_filenames(self) -> dict[str, int]:
        """One-time rename pass: legacy `actor_NNNN.jpg` → `{ethnicity}__{gender}__{age_range}.jpg`.

        Per follow-up 033: idempotent. For each `_actors/actor_NNNN/` folder
        that still has the legacy name, parse the sidecar to recover attrs
        and rename the jpg. Folders already on the new naming are skipped.
        Per-folder OSError is logged and counted as an error but does not
        abort the sweep — startup must not block on a single bad file.
        Skips `_deleted/_actors/` (soft-deleted actors are left alone).
        """
        result = {"migrated": 0, "skipped": 0, "errors": 0}
        actors_dir = self.actors_dir()
        if not actors_dir.is_dir():
            return result
        for entry in actors_dir.iterdir():
            if not entry.is_dir() or entry.is_symlink():
                continue
            if not _ACTOR_DIR_RE.match(entry.name):
                continue
            # Already migrated?
            already = any(
                child.is_file() and _NEW_FILENAME_RE.match(child.name)
                for child in entry.iterdir()
            )
            if already:
                result["skipped"] += 1
                continue
            legacy = entry / f"{entry.name}.jpg"
            md = entry / f"{entry.name}.md"
            if not legacy.is_file() or not md.is_file():
                result["skipped"] += 1
                continue
            attrs = self._parse_sidecar(md)
            if attrs is None:
                result["errors"] += 1
                continue
            new_path = entry / _attrs_to_filename(attrs)
            if new_path.exists():
                result["skipped"] += 1
                continue
            try:
                legacy.rename(new_path)
                result["migrated"] += 1
            except OSError:
                result["errors"] += 1
        return result

    def delete_actor(self, actor_id: str) -> dict[str, str]:
        """Soft-delete an actor folder to ai_videos/_deleted/_actors/actor_NNNN/.

        Per follow-up 026: mirrors the mp4 delete pattern (follow-up 023) at
        folder granularity. Atomic Path.rename. Casting cleanup is the
        caller's responsibility (the endpoint runs Casting.unassign_actor_everywhere
        before this rename so dangling references never persist).
        """
        if not _ACTOR_ID_RE.match(actor_id):
            raise InvalidAttribute(f"actor_id={actor_id!r} does not match shape")
        src = self.actors_dir() / actor_id
        if not src.is_dir() or src.is_symlink():
            raise ActorNotFound(f"actor folder not found: {actor_id}")
        target = self._resolver.root / "ai_videos" / "_deleted" / ACTORS_DIR_NAME / actor_id
        if target.exists():
            raise ActorDeleteTargetExists(self._rel(target))
        try:
            target.parent.mkdir(parents=True, exist_ok=True)
        except OSError as exc:
            raise ActorDeleteFailed(f"mkdir failed: {exc}") from exc
        try:
            src.rename(target)
        except OSError as exc:
            raise ActorDeleteFailed(f"rename failed: {exc}") from exc
        return {"from": self._rel(src), "to": self._rel(target)}

