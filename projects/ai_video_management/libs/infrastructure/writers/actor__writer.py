@dataclass(frozen=True)
class Variance:
    """Per-image variance bundle returned by `_variance_for` (follow-up 052).

    Splits the formerly-flat string into named slots so face/body prompt
    builders can compose deterministically AND share the same variance
    across both shots (same actor identity across face+body when seed is
    locked). `features_text` carries the original 17-pool micro-feature
    variance (follow-up 029/031); the other fields are style-level
    additions introduced in follow-up 052 (realism + diversity strategy).
    """

    medium: str
    photographer: str
    type_anchor: str
    framing_face: str
    lighting_paradigm: str
    features_text: str
    negatives_emphasis: str


def _variance_for(seed: int, gender: str, archetype: str | None = None) -> Variance:
    """Build a per-image variance bundle (follow-up 029 + 031 + 052).

    Same seed reproduces the same `Variance`. The `features_text` slot draws
    multiple fragments from 17 micro-feature pools (look archetype + 6
    facial parts + 3 hair sub-axes + 2 skin sub-axes + expression / mood /
    lighting / photography) and is held to ≥ 1000 chars by a tail guard.
    The five new fields (medium / photographer / type_anchor / framing_face
    / lighting_paradigm) are picked from style-level pools introduced by
    follow-up 052 to break Kling's macro-level same-face convergence. The
    `negatives_emphasis` rotates 2-3 entries from `_VARIANCE_NEGATIVE_ROTATION`
    per gen so the negative prompt doesn't anchor a single anti-pattern.
    """
    rng = random.Random(seed)
    look_pool = _VARIANCE_LOOK_FEMALE if gender == "female" else _VARIANCE_LOOK_MALE
    features_pool = (
        _VARIANCE_FACE_FEATURES_FEMALE if gender == "female" else _VARIANCE_FACE_FEATURES_MALE
    )
    type_pool = (
        _VARIANCE_TYPE_ANCHOR_FEMALE if gender == "female" else _VARIANCE_TYPE_ANCHOR_MALE
    )
    parts: list[str] = []
    parts.append(rng.choice(look_pool))
    parts.extend(rng.sample(features_pool, k=min(2, len(features_pool))))
    # Per follow-up 069: archetype-biased picks for the 6 facial-feature
    # pools so e.g. 英俊男主 lands square jawline + chiseled cheeks + phoenix
    # eyes; 妖艳女配 lands V-shaped jaw + hollow cheeks + heavy-lidded eyes.
    # Falls through to uniform random when archetype is None or unknown.
    bias = _ARCHETYPE_FEATURE_BIAS.get(archetype or "", {})
    parts.append(_pick_biased(rng, _VARIANCE_JAWLINE,    bias.get("jawline")))
    parts.append(_pick_biased(rng, _VARIANCE_CHEEKBONES, bias.get("cheekbones")))
    parts.append(_pick_biased(rng, _VARIANCE_BROW,       bias.get("brow")))
    parts.append(_pick_biased(rng, _VARIANCE_NOSE,       bias.get("nose")))
    parts.append(_pick_biased(rng, _VARIANCE_LIPS,       bias.get("lips")))
    # Eyes still draws 2 distinct entries; if biased, sample from the bias
    # subset (deduplicated against pool length).
    eye_bias = bias.get("eyes")
    if eye_bias:
        eye_candidates = [_VARIANCE_EYES[i] for i in eye_bias if 0 <= i < len(_VARIANCE_EYES)]
        k = min(2, len(eye_candidates))
        parts.extend(rng.sample(eye_candidates, k=k) if k else [])
    else:
        parts.extend(rng.sample(_VARIANCE_EYES, k=min(2, len(_VARIANCE_EYES))))
    parts.append(rng.choice(_VARIANCE_HAIR_LENGTH))
    parts.append(rng.choice(_VARIANCE_HAIR_STYLE))
    parts.append(rng.choice(_VARIANCE_HAIR_COLOR))
    parts.append(rng.choice(_VARIANCE_SKIN_TONE))
    parts.append(rng.choice(_VARIANCE_SKIN_TEXTURE))
    parts.extend(rng.sample(_VARIANCE_EXPRESSION, k=min(2, len(_VARIANCE_EXPRESSION))))
    parts.append(rng.choice(_VARIANCE_MOOD))
    parts.extend(rng.sample(_VARIANCE_LIGHTING, k=min(2, len(_VARIANCE_LIGHTING))))
    parts.extend(rng.sample(_VARIANCE_PHOTOGRAPHY, k=min(3, len(_VARIANCE_PHOTOGRAPHY))))
    # Per follow-up 072: strip CJK-in-parens annotations baked into the new
    # follow-up 071 pool entries (e.g. " (高挺鼻梁)", " (小眼睛)"). They serve
    # as in-source documentation but the Kling text-to-image API rejects
    # prompts containing them — observed as `kling submit: code=1101…` →
    # surfaced to the actor batch as HTTP 500 per slot. Strip happens once
    # at assembly time so the per-pool source comments remain useful for
    # devs browsing the file.
    features_text = _CJK_PARENS_RE.sub("", ", ".join(parts))
    while len(features_text) < 1000:
        features_text = features_text + ", " + rng.choice(_VARIANCE_PHOTOGRAPHY)
    negatives = rng.sample(
        _VARIANCE_NEGATIVE_ROTATION, k=min(3, len(_VARIANCE_NEGATIVE_ROTATION))
    )
    return Variance(
        medium=rng.choice(_VARIANCE_PHOTOREALISM),
        photographer=rng.choice(_VARIANCE_PHOTOGRAPHER_STYLE),
        type_anchor=rng.choice(type_pool),
        framing_face=rng.choice(_VARIANCE_FRAMING_FACE),
        lighting_paradigm=rng.choice(_VARIANCE_LIGHTING_PARADIGM),
        features_text=features_text,
        negatives_emphasis=", ".join(negatives),
    )


