def _find_actor_jpg(actor_folder: Path) -> Path | None:
    """Return the actor's jpg file path, preferring the follow-up 033
    descriptive naming over the legacy `{actor_id}.jpg` name. Returns None
    if the folder contains no jpg."""
    for child in actor_folder.iterdir() if actor_folder.is_dir() else []:
        if child.is_file() and _NEW_FILENAME_RE.match(child.name):
            return child
    # Fallback: legacy `{actor_id}.jpg` for not-yet-migrated folders.
    legacy = actor_folder / f"{actor_folder.name}.jpg"
    if legacy.is_file():
        return legacy
    return None

