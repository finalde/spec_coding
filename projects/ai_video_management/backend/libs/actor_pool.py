def _b64url(data: bytes) -> str:
    """URL-safe base64 without padding (JWT segment encoding)."""
    return base64.urlsafe_b64encode(data).rstrip(b"=").decode("ascii")


def _make_kling_jwt(access_key: str, secret_key: str, exp_seconds: int = KLING_JWT_EXP_SECONDS) -> str:
    """Build an HS256-signed JWT for Kling API auth, using only stdlib.

    Avoids `PyJWT` dependency. The Kling spec requires iss/exp/nbf claims.
    """
    header = {"alg": "HS256", "typ": "JWT"}
    now = int(time.time())
    payload = {"iss": access_key, "exp": now + exp_seconds, "nbf": now - 5}
    header_b64 = _b64url(json.dumps(header, separators=(",", ":")).encode("utf-8"))
    payload_b64 = _b64url(json.dumps(payload, separators=(",", ":")).encode("utf-8"))
    signing_input = f"{header_b64}.{payload_b64}".encode("ascii")
    signature = hmac.new(secret_key.encode("utf-8"), signing_input, hashlib.sha256).digest()
    sig_b64 = _b64url(signature)
    return f"{header_b64}.{payload_b64}.{sig_b64}"


def _kling_aspect_ratio(width: int, height: int) -> str:
    """Map (width, height) to a Kling-accepted aspect ratio enum.

    Kling does not accept arbitrary dims; it accepts a fixed set of ratio
    enums. Approximate by closest-match.
    """
    if height == 0:
        return "1:1"
    ratio = width / height
    candidates = {
        "1:1": 1.0,
        "16:9": 16 / 9,
        "9:16": 9 / 16,
        "4:3": 4 / 3,
        "3:4": 3 / 4,
    }
    return min(candidates, key=lambda k: abs(candidates[k] - ratio))


def _is_safe_download_host(url: str) -> bool:
    """Reject SSRF-prone hostnames (localhost, private ranges) on Kling's CDN URL."""
    try:
        parsed = urlparse(url)
    except ValueError:
        return False
    if parsed.scheme != "https":
        return False
    host = parsed.hostname
    if not host:
        return False
    try:
        addr_infos = socket.getaddrinfo(host, None)
    except OSError:
        return False
    for info in addr_infos:
        sockaddr = info[4]
        ip_str = sockaddr[0]
        try:
            ip = ipaddress.ip_address(ip_str)
        except ValueError:
            continue
        if ip.is_loopback or ip.is_private or ip.is_link_local or ip.is_multicast or ip.is_reserved:
            return False
    return True


class KlingProvider:
    """Async POST→poll→download against api.klingai.com text-to-image.

    Flow:
      1. POST /images/generations  →  {code: 0, data: {task_id: <uuid>}}
      2. Poll GET /images/generations?pageSize=500 every 2s; find our task
         by task_id; wait for task_status="succeed" (or "failed" → raise).
      3. task_result.images[0].url → SSRF-vet → download bytes.
    """

    name: str = "kling"

    def __init__(
        self,
        access_key: str,
        secret_key: str,
        model: str = KLING_DEFAULT_MODEL,
        base_url: str = KLING_BASE_URL,
        poll_interval: float = KLING_POLL_INTERVAL_SECONDS,
        max_wait: float = KLING_MAX_WAIT_SECONDS,
    ) -> None:
        if not access_key or not secret_key:
            raise ValueError("KlingProvider requires both access_key and secret_key")
        self._ak = access_key
        self._sk = secret_key
        self._model = model
        self._base = base_url.rstrip("/")
        self._poll_interval = poll_interval
        self._max_wait = max_wait

    @classmethod
    def from_env(cls) -> "KlingProvider | None":
        ak = os.environ.get(KLING_ACCESS_KEY_ENV, "").strip()
        sk = os.environ.get(KLING_SECRET_KEY_ENV, "").strip()
        if not ak or not sk:
            return None
        return cls(ak, sk)

    def generate(self, prompt: str, seed: int, width: int, height: int) -> bytes:
        token = _make_kling_jwt(self._ak, self._sk)
        with httpx.Client(timeout=DEFAULT_TIMEOUT_SECONDS, follow_redirects=False) as client:
            task_id = self._submit(client, token, prompt, seed, width, height)
            img_url = self._poll(client, token, task_id)
        if not _is_safe_download_host(img_url):
            raise RuntimeError(f"kling: rejected unsafe download URL: {img_url}")
        with httpx.Client(timeout=DEFAULT_TIMEOUT_SECONDS, follow_redirects=True) as client:
            with client.stream("GET", img_url) as resp:
                resp.raise_for_status()
                buf = bytearray()
                for chunk in resp.iter_bytes():
                    buf.extend(chunk)
                    if len(buf) > MAX_RESPONSE_BYTES:
                        raise ValueError(
                            f"kling: response_too_large (>{MAX_RESPONSE_BYTES} bytes)"
                        )
                return bytes(buf)

    def _submit(
        self,
        client: httpx.Client,
        token: str,
        prompt: str,
        seed: int,
        width: int,
        height: int,
    ) -> str:
        body: dict[str, object] = {
            "model_name": self._model,
            "prompt": prompt,
            "aspect_ratio": _kling_aspect_ratio(width, height),
            "n": 1,
        }
        resp = client.post(
            f"{self._base}/images/generations",
            json=body,
            headers={
                "Authorization": f"Bearer {token}",
                "Content-Type": "application/json",
            },
        )
        resp.raise_for_status()
        payload = resp.json()
        if payload.get("code") != 0:
            raise RuntimeError(
                f"kling submit: code={payload.get('code')} message={payload.get('message')!r}"
            )
        data = payload.get("data") or {}
        task_id = data.get("task_id")
        if not isinstance(task_id, str) or not task_id:
            raise RuntimeError(f"kling submit: missing task_id in {payload!r}")
        return task_id

    def _poll(self, client: httpx.Client, token: str, task_id: str) -> str:
        start = time.time()
        headers = {"Authorization": f"Bearer {token}"}
        while True:
            if time.time() - start > self._max_wait:
                raise TimeoutError(
                    f"kling: task {task_id} not done in {self._max_wait:.0f}s"
                )
            resp = client.get(
                f"{self._base}/images/generations",
                params={"pageSize": 500},
                headers=headers,
            )
            resp.raise_for_status()
            tasks = (resp.json() or {}).get("data") or []
            for task in tasks:
                if task.get("task_id") != task_id:
                    continue
                status = task.get("task_status")
                if status == "succeed":
                    images = (task.get("task_result") or {}).get("images") or []
                    if not images:
                        raise RuntimeError(
                            f"kling: task {task_id} succeeded with no images"
                        )
                    url = images[0].get("url")
                    if not isinstance(url, str) or not url:
                        raise RuntimeError(
                            f"kling: task {task_id} missing image url"
                        )
                    return url
                if status == "failed":
                    msg = task.get("task_status_msg") or task
                    raise RuntimeError(f"kling: task {task_id} failed: {msg!r}")
                break
            time.sleep(self._poll_interval)


class ActorPool:
    def __init__(
        self,
        exposed: ExposedTree,
        resolver: SafeResolver,
        provider: KlingProvider | None = None,
    ) -> None:
        self._exposed = exposed
        self._resolver = resolver
        if provider is not None:
            self._provider = provider
        else:
            from_env = KlingProvider.from_env()
            if from_env is None:
                raise RuntimeError(
                    f"kling env keys missing; set {KLING_ACCESS_KEY_ENV} + "
                    f"{KLING_SECRET_KEY_ENV} (e.g. via backend/.env loaded by env_loader)"
                )
            self._provider = from_env
        # Per follow-up 033: idempotent one-time filename migration. Cheap
        # when nothing to do (single iterdir on `_actors/`); only acts on
        # folders still carrying the legacy `actor_id.jpg` name.
        try:
            self.migrate_filenames()
        except Exception:
            # Migration is best-effort: must never block app startup.
            pass

    @property
    def provider(self) -> KlingProvider:
        return self._provider

    def actors_dir(self) -> Path:
        return self._exposed.root / "ai_videos" / ACTORS_DIR_NAME

    def preview_prompts(
        self,
        attrs: ActorAttrs,
        count: int,
        resolution: str = DEFAULT_RESOLUTION,
    ) -> dict[str, object]:
        """Dry-run prompt preview (follow-up 032).

        Computes the same {seed, prompt} pairs that `generate_batch` would
        send to Kling, but without allocating actor folders, calling Kling,
        or writing files. The caller can show the user the exact prompts
        and then re-fire the actual generate with `seeds=[...]` set to the
        previewed seeds — that path produces byte-equal Kling requests.
        """
        self._validate_attrs(attrs)
        if not MIN_BATCH_COUNT <= count <= MAX_BATCH_COUNT:
            raise InvalidAttribute(
                f"count must be between {MIN_BATCH_COUNT} and {MAX_BATCH_COUNT}, got {count}"
            )
        if resolution not in RESOLUTION_OPTIONS:
            raise InvalidAttribute(
                f"resolution={resolution!r} not in {sorted(RESOLUTION_OPTIONS)}"
            )
        base_seed = int(time.time() * 1000)
        prompts: list[dict[str, object]] = []
        for i in range(count):
            seed = base_seed + i
            variance = _variance_for(seed, attrs.gender)
            prompt = self._build_prompt(attrs, variance=variance)
            prompts.append({"seed": seed, "prompt": prompt})
        return {"prompts": prompts, "resolution": resolution}

    def generate_batch(
        self,
        attrs: ActorAttrs,
        count: int,
        resolution: str = DEFAULT_RESOLUTION,
        seeds: list[int] | None = None,
    ) -> GenerateResult:
        self._validate_attrs(attrs)
        if not MIN_BATCH_COUNT <= count <= MAX_BATCH_COUNT:
            raise InvalidAttribute(
                f"count must be between {MIN_BATCH_COUNT} and {MAX_BATCH_COUNT}, got {count}"
            )
        if resolution not in RESOLUTION_OPTIONS:
            raise InvalidAttribute(
                f"resolution={resolution!r} not in {sorted(RESOLUTION_OPTIONS)}"
            )
        if seeds is not None:
            if not isinstance(seeds, list) or len(seeds) != count:
                raise InvalidAttribute(
                    f"seeds must be a list of length {count}, got {seeds!r}"
                )
            for s in seeds:
                if not isinstance(s, int):
                    raise InvalidAttribute(f"seeds must be all int, got {seeds!r}")
        actors_dir = self.actors_dir()
        try:
            actors_dir.mkdir(parents=True, exist_ok=True)
        except OSError as exc:
            raise GenerationDirMissing(str(exc))

        result = GenerateResult()
        base_seed = int(time.time() * 1000)
        target_px = _RESOLUTION_PRESETS[resolution]
        self._reap_incomplete_folders(actors_dir)
        for i in range(count):
            seed = seeds[i] if seeds is not None else base_seed + i
            try:
                actor_id, actor_folder = self._allocate_actor_id(actors_dir)
            except GenerationDirMissing as exc:
                result.errors.append({"requested_id": f"slot_{i}", "message": f"alloc_failed: {exc}"})
                continue
            variance = _variance_for(seed, attrs.gender)
            prompt = self._build_prompt(attrs, variance=variance)
            try:
                image_bytes = self._provider.generate(prompt, seed, IMAGE_WIDTH, IMAGE_HEIGHT)
            except Exception as exc:
                result.errors.append({"requested_id": actor_id, "message": f"http_failed: {exc}"})
                self._cleanup_empty_folder(actor_folder)
                continue
            if not image_bytes:
                result.errors.append({"requested_id": actor_id, "message": "empty_response"})
                self._cleanup_empty_folder(actor_folder)
                continue
            if target_px is not None:
                try:
                    image_bytes = self._resize_jpeg(image_bytes, target_px)
                except Exception as exc:
                    result.errors.append({"requested_id": actor_id, "message": f"resize_failed: {exc}"})
                    self._cleanup_empty_folder(actor_folder)
                    continue
            jpg_path = actor_folder / _attrs_to_filename(attrs)
            md_path = actor_folder / f"{actor_id}.md"
            try:
                jpg_path.write_bytes(image_bytes)
                md_path.write_text(
                    self._build_sidecar(actor_id, attrs, prompt, seed, resolution),
                    encoding="utf-8",
                )
            except OSError as exc:
                result.errors.append({"requested_id": actor_id, "message": f"write_failed: {exc}"})
                self._cleanup_empty_folder(actor_folder)
                continue
            rel_image = self._rel(jpg_path)
            result.generated.append(
                {
                    "id": actor_id,
                    "image_path": rel_image,
                    "attrs": attrs.to_dict(),
                    "seed": seed,
                    "resolution": resolution,
                }
            )
        return result

    @staticmethod
    def _resize_jpeg(jpeg_bytes: bytes, target_px: int) -> bytes:
        """Resize a JPEG to target_px × target_px using Lanczos resampling.

        Per follow-up 029: Kling returns ~1024×1024 natively for 1:1 aspect;
        the user-pickable resolution dropdown ("2k"/"4k") forces an explicit
        upscale via Pillow's high-quality LANCZOS filter so the saved file
        matches the requested pixel count. Upscaling does not add true
        detail — it is interpolation; recorded in the sidecar.
        """
        img = Image.open(BytesIO(jpeg_bytes)).convert("RGB")
        img = img.resize((target_px, target_px), Image.LANCZOS)
        buf = BytesIO()
        img.save(buf, format="JPEG", quality=JPEG_QUALITY)
        return buf.getvalue()

    def list_actors(self) -> list[ActorInfo]:
        actors_dir = self.actors_dir()
        if not actors_dir.is_dir():
            return []
        out: list[ActorInfo] = []
        for child in sorted(actors_dir.iterdir(), key=lambda p: p.name):
            if not child.is_dir() or child.is_symlink():
                continue
            match = _ACTOR_DIR_RE.match(child.name)
            if not match:
                continue
            actor_id = child.name
            md_path = child / f"{actor_id}.md"
            if not md_path.is_file():
                continue
            jpg_path = _find_actor_jpg(child)
            if jpg_path is None:
                continue
            attrs = self._parse_sidecar(md_path)
            if attrs is None:
                continue
            try:
                mtime = jpg_path.stat().st_mtime
            except OSError:
                continue
            out.append(ActorInfo(id=actor_id, attrs=attrs, image_path=self._rel(jpg_path), mtime=mtime))
        return out

    def actor_exists(self, actor_id: str) -> bool:
        if not _ACTOR_ID_RE.match(actor_id):
            return False
        folder = self.actors_dir() / actor_id
        if not folder.is_dir():
            return False
        return _find_actor_jpg(folder) is not None

    def migrate_filenames(self) -> dict[str, int]:
        """One-time rename pass: legacy `actor_NNNN.jpg` → `{ethnicity}__{gender}__{age_range}.jpg`.

        Per follow-up 033: idempotent. For each `_actors/actor_NNNN/` folder
        that still has the legacy name, parse the sidecar to recover attrs
        and rename the jpg. Folders already on the new naming are skipped.
        Per-folder OSError is logged and counted as an error but does not
        abort the sweep — startup must not block on a single bad file.
        Skips `_deleted/_actors/` (soft-deleted actors are left alone).
        """
        result = {"migrated": 0, "skipped": 0, "errors": 0}
        actors_dir = self.actors_dir()
        if not actors_dir.is_dir():
            return result
        for entry in actors_dir.iterdir():
            if not entry.is_dir() or entry.is_symlink():
                continue
            if not _ACTOR_DIR_RE.match(entry.name):
                continue
            # Already migrated?
            already = any(
                child.is_file() and _NEW_FILENAME_RE.match(child.name)
                for child in entry.iterdir()
            )
            if already:
                result["skipped"] += 1
                continue
            legacy = entry / f"{entry.name}.jpg"
            md = entry / f"{entry.name}.md"
            if not legacy.is_file() or not md.is_file():
                result["skipped"] += 1
                continue
            attrs = self._parse_sidecar(md)
            if attrs is None:
                result["errors"] += 1
                continue
            new_path = entry / _attrs_to_filename(attrs)
            if new_path.exists():
                result["skipped"] += 1
                continue
            try:
                legacy.rename(new_path)
                result["migrated"] += 1
            except OSError:
                result["errors"] += 1
        return result

    def delete_actor(self, actor_id: str) -> dict[str, str]:
        """Soft-delete an actor folder to ai_videos/_deleted/_actors/actor_NNNN/.

        Per follow-up 026: mirrors the mp4 delete pattern (follow-up 023) at
        folder granularity. Atomic Path.rename. Casting cleanup is the
        caller's responsibility (the endpoint runs Casting.unassign_actor_everywhere
        before this rename so dangling references never persist).
        """
        if not _ACTOR_ID_RE.match(actor_id):
            raise InvalidAttribute(f"actor_id={actor_id!r} does not match shape")
        src = self.actors_dir() / actor_id
        if not src.is_dir() or src.is_symlink():
            raise ActorNotFound(f"actor folder not found: {actor_id}")
        target = self._resolver.root / "ai_videos" / "_deleted" / ACTORS_DIR_NAME / actor_id
        if target.exists():
            raise ActorDeleteTargetExists(self._rel(target))
        try:
            target.parent.mkdir(parents=True, exist_ok=True)
        except OSError as exc:
            raise ActorDeleteFailed(f"mkdir failed: {exc}") from exc
        try:
            src.rename(target)
        except OSError as exc:
            raise ActorDeleteFailed(f"rename failed: {exc}") from exc
        return {"from": self._rel(src), "to": self._rel(target)}

    @staticmethod
    def _validate_attrs(attrs: ActorAttrs) -> None:
        if attrs.ethnicity not in ETHNICITY_OPTIONS:
            raise InvalidAttribute(f"ethnicity={attrs.ethnicity!r} not in schema")
        if attrs.gender not in GENDER_OPTIONS:
            raise InvalidAttribute(f"gender={attrs.gender!r} not in schema")
        if attrs.age_range not in AGE_RANGE_OPTIONS:
            raise InvalidAttribute(f"age_range={attrs.age_range!r} not in schema")
        if attrs.look not in LOOK_OPTIONS:
            raise InvalidAttribute(f"look={attrs.look!r} not in schema")
        if attrs.style not in STYLE_OPTIONS:
            raise InvalidAttribute(f"style={attrs.style!r} not in schema")
        if len(attrs.notes) > 500:
            raise InvalidAttribute("notes must be ≤ 500 characters")

    @staticmethod
    def _build_prompt(attrs: ActorAttrs, variance: str = "") -> str:
        """Build the Kling prompt with anti-wax / anti-AI framing (follow-up 031).

        Removes the prior "photorealistic, sharp focus, 8k" trio — those tokens
        are strongly correlated with CG/render aesthetics in image-model
        training data and tend to nudge Kling toward smooth waxwork faces.
        Replaced with candid-photograph + natural-skin-texture cues so every
        image starts from a "real person photographed by a human" baseline.
        Variance still injects per-image difference between look and style.
        """
        ethn = _ETHNICITY_PROMPT_FRAGMENT[attrs.ethnicity]
        age = _AGE_PROMPT_FRAGMENT[attrs.age_range]
        style = _STYLE_PROMPT_FRAGMENT[attrs.style]
        parts = [
            f"candid unposed portrait photograph of {ethn} {attrs.gender}",
            age,
            attrs.look,
        ]
        if variance:
            parts.append(variance)
        parts.extend([
            style,
            "natural ambient lighting",
            "neutral uncluttered background",
            "natural skin texture with visible pores and subtle imperfections",
            "slight natural facial asymmetry",
            "RAW unedited photograph aesthetic",
            "no plastic skin, no waxy smoothness, no symmetrical perfection, no CG render look",
        ])
        return ", ".join(parts)

    def _allocate_actor_id(self, actors_dir: Path) -> tuple[str, Path]:
        """Atomically claim the next free actor_NNNN id via mkdir(exist_ok=False).

        Per follow-up 027: replaces the old pre-computed `next_id + offset`
        pattern, which lost to itself when 9 concurrent count=1 requests
        all saw the same starting id and raced on the same mkdir. Now each
        allocation walks forward through ids using mkdir as the atomic
        primitive — first thread to win mkdir on actor_K owns it, others
        try actor_K+1, K+2, ... up to _MAX_ID_ALLOC_SCAN attempts.
        """
        start = self._next_actor_id_num(actors_dir)
        candidate = start
        attempts = 0
        while attempts < _MAX_ID_ALLOC_SCAN:
            actor_id = f"actor_{candidate:04d}"
            actor_folder = actors_dir / actor_id
            try:
                actor_folder.mkdir(parents=True, exist_ok=False)
                return actor_id, actor_folder
            except FileExistsError:
                candidate += 1
                attempts += 1
                continue
            except OSError as exc:
                raise GenerationDirMissing(str(exc)) from exc
        raise GenerationDirMissing(
            f"exhausted {_MAX_ID_ALLOC_SCAN} id-allocation attempts starting from actor_{start:04d}"
        )

    @staticmethod
    def _reap_incomplete_folders(actors_dir: Path) -> None:
        """Drop actor_NNNN folders left behind by killed batches (no jpg).

        Per follow-up 018 + 027 + 033: "no jpg" check now uses
        `_find_actor_jpg` which matches both the follow-up 033 descriptive
        filename and the legacy `actor_id.jpg`, so a partly-migrated tree
        does not get reaped as "incomplete".
        """
        try:
            entries = list(actors_dir.iterdir())
        except OSError:
            return
        for entry in entries:
            if not entry.is_dir():
                continue
            if not _ACTOR_DIR_RE.match(entry.name):
                continue
            if _find_actor_jpg(entry) is not None:
                continue
            try:
                for child in entry.iterdir():
                    try:
                        child.unlink()
                    except OSError:
                        pass
                entry.rmdir()
            except OSError:
                pass

    @staticmethod
    def _next_actor_id_num(actors_dir: Path) -> int:
        """Return max+1 across every actor_NNNN folder under actors_dir.

        Pure scan with no side effects (per follow-up 027: reaping moved
        to `_reap_incomplete_folders` so the allocator's mkdir is the
        sole atomic primitive). A folder counts as occupied regardless of
        jpg presence — if a folder exists, its id is taken.
        """
        max_num = 0
        try:
            entries = list(actors_dir.iterdir())
        except OSError:
            return 1
        for entry in entries:
            if not entry.is_dir():
                continue
            m = _ACTOR_DIR_RE.match(entry.name)
            if not m:
                continue
            n = int(m.group(1))
            if n > max_num:
                max_num = n
        return max_num + 1

    @staticmethod
    def _build_sidecar(
        actor_id: str,
        attrs: ActorAttrs,
        prompt: str,
        seed: int,
        resolution: str = DEFAULT_RESOLUTION,
    ) -> str:
        notes_display = attrs.notes if attrs.notes else "—"
        return (
            f"# {actor_id}\n"
            f"\n"
            f"AI-generated actor face (Kling text-to-image, follow-up 025 + 029).\n"
            f"\n"
            f"| 字段 | 值 |\n"
            f"|---|---|\n"
            f"| ethnicity | {attrs.ethnicity} |\n"
            f"| gender | {attrs.gender} |\n"
            f"| age_range | {attrs.age_range} |\n"
            f"| look | {attrs.look} |\n"
            f"| style | {attrs.style} |\n"
            f"| notes | {notes_display} |\n"
            f"| seed | {seed} |\n"
            f"| resolution | {resolution} |\n"
            f"\n"
            f"## 生成 prompt\n"
            f"\n"
            f"```\n"
            f"{prompt}\n"
            f"```\n"
        )

    @staticmethod
    def _parse_sidecar(md_path: Path) -> ActorAttrs | None:
        try:
            text = md_path.read_text(encoding="utf-8")
        except OSError:
            return None
        fields: dict[str, str] = {}
        for line in text.splitlines():
            line = line.strip()
            if not line.startswith("|"):
                continue
            cells = [c.strip() for c in line.strip("|").split("|")]
            if len(cells) != 2:
                continue
            key, value = cells[0], cells[1]
            if key in {"ethnicity", "gender", "age_range", "look", "style", "notes"}:
                fields[key] = value if value != "—" else ""
        try:
            return ActorAttrs(
                ethnicity=fields["ethnicity"],
                gender=fields["gender"],
                age_range=fields["age_range"],
                look=fields["look"],
                style=fields["style"],
                notes=fields.get("notes", ""),
            )
        except KeyError:
            return None

    def _rel(self, p: Path) -> str:
        try:
            return p.resolve().relative_to(self._resolver.root).as_posix()
        except (OSError, ValueError):
            return p.as_posix()

    @staticmethod
    def _cleanup_empty_folder(folder: Path) -> None:
        try:
            if folder.is_dir() and not any(folder.iterdir()):
                folder.rmdir()
        except OSError:
            pass


