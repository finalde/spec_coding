def _variance_for(seed: int, gender: str) -> str:
    """Build a per-image variance phrase ≥ 1000 chars (follow-up 029).

    Draws multiple fragments from 17 pools (look archetype + 6 facial parts +
    3 hair sub-axes + 2 skin sub-axes + expression / mood / lighting /
    photography) using a seeded RNG so the same seed reproduces the same
    variance. Picks per-pool counts are tuned to land in the 1000-1500 char
    range; the length guard at the bottom appends more photography
    descriptors if the assembled string falls short.
    """
    rng = random.Random(seed)
    look_pool = _VARIANCE_LOOK_FEMALE if gender == "female" else _VARIANCE_LOOK_MALE
    features_pool = (
        _VARIANCE_FACE_FEATURES_FEMALE if gender == "female" else _VARIANCE_FACE_FEATURES_MALE
    )
    parts: list[str] = []
    parts.append(rng.choice(look_pool))
    parts.extend(rng.sample(features_pool, k=min(2, len(features_pool))))
    parts.append(rng.choice(_VARIANCE_JAWLINE))
    parts.append(rng.choice(_VARIANCE_CHEEKBONES))
    parts.append(rng.choice(_VARIANCE_BROW))
    parts.append(rng.choice(_VARIANCE_NOSE))
    parts.append(rng.choice(_VARIANCE_LIPS))
    parts.extend(rng.sample(_VARIANCE_EYES, k=min(2, len(_VARIANCE_EYES))))
    parts.append(rng.choice(_VARIANCE_HAIR_LENGTH))
    parts.append(rng.choice(_VARIANCE_HAIR_STYLE))
    parts.append(rng.choice(_VARIANCE_HAIR_COLOR))
    parts.append(rng.choice(_VARIANCE_SKIN_TONE))
    parts.append(rng.choice(_VARIANCE_SKIN_TEXTURE))
    parts.extend(rng.sample(_VARIANCE_EXPRESSION, k=min(2, len(_VARIANCE_EXPRESSION))))
    parts.append(rng.choice(_VARIANCE_MOOD))
    parts.extend(rng.sample(_VARIANCE_LIGHTING, k=min(2, len(_VARIANCE_LIGHTING))))
    parts.extend(rng.sample(_VARIANCE_PHOTOGRAPHY, k=min(3, len(_VARIANCE_PHOTOGRAPHY))))
    parts.extend(rng.sample(_VARIANCE_PHOTOREALISM, k=min(2, len(_VARIANCE_PHOTOREALISM))))
    result = ", ".join(parts)
    # 1000-char floor: append more photography descriptors if the pool draws
    # happened to land short. In practice the default schedule yields ~1100
    # chars; this is a safety net, not the normal path.
    while len(result) < 1000:
        extra = rng.choice(_VARIANCE_PHOTOGRAPHY)
        result = result + ", " + extra
    return result


